package com.example.helloworld;

import static org.hamcrest.Matchers.containsString;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.post;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.content;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

import org.junit.Test;
import org.junit.runner.RunWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.WebMvcTest;
import org.springframework.test.context.junit4.SpringRunner;
import org.springframework.test.web.servlet.MockMvc;

@RunWith(SpringRunner.class)
@WebMvcTest(CloudEventController.class)
public class CloudEventControllerTest {

  @Autowired private MockMvc mockMvc;

  @Test
  public void shouldParseData() throws Exception {
    String content = "\n\nYXZlcmkK1234test"; // Binary encoded proto message

    mockMvc
        .perform(post("/").contentType("application/x-protobuf").content(content))
        .andExpect(status().isOk())
        .andExpect(content().string(containsString("Event received!")));
  }
}
